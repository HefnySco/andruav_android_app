public class AndruavVector3D {

    /***
     * ranges from -1.0 to 1.0
     */
    public double X;
    public double Y;
    public double Z;


    public AndruavVector3D ()
    {

    }

    public AndruavVector3D (final double  x, final double y, final double  z)
    {
        X = x;
        Y = y;
        Z = z;
    }


}
