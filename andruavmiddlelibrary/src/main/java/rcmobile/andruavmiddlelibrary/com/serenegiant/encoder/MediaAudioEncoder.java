import android.media.AudioFormat;
import android.media.AudioRecord;
import android.media.MediaCodec;
import android.media.MediaCodecInfo;
import android.media.MediaCodecList;
import android.media.MediaFormat;
import android.media.MediaRecorder;
import android.os.Build;
import android.util.Log;

import java.io.IOException;
import java.nio.ByteBuffer;

public class MediaAudioEncoder extends MediaEncoder {
	private static final boolean DEBUG = true;	// TODO set false on release
	private static final String TAG = "MediaAudioEncoder";


	private static final String MIME_TYPE = "audio/mp4a-latm";
	private static final int SAMPLE_RATE = 44100;	// 44.1[KHz] is only setting guaranteed to be available on all devices.
	private static final int BIT_RATE = 64000;

	private rcmobile.andruavmiddlelibrary.com.serenegiant.encoder.MediaAudioEncoder.AudioThread mAudioThread = null;
	private boolean mEncodeCalled  = false;

	public MediaAudioEncoder(final MediaMuxerWrapper muxer, final MediaEncoderListener listener) {
		super(muxer, listener);
	}


	@Override
	protected void prepare(int x, int y, int z) throws IOException {
		if (DEBUG) Log.v(TAG, "prepare:");
		mTrackIndex = -1;
		mMuxerStarted = mIsEOS = false;
		// prepare MediaCodec for AAC encoding of audio data from inernal mic.
		final MediaCodecInfo audioCodecInfo = selectAudioCodec(MIME_TYPE);
		if (audioCodecInfo == null) {
			Log.e(TAG, "Unable to find an appropriate codec for " + MIME_TYPE);
			return;
		}
		if (DEBUG) Log.i(TAG, "selected codec: " + audioCodecInfo.getName());

		final MediaFormat audioFormat = MediaFormat.createAudioFormat(MIME_TYPE, SAMPLE_RATE, 1);
		audioFormat.setInteger(MediaFormat.KEY_AAC_PROFILE, MediaCodecInfo.CodecProfileLevel.AACObjectLC);
		audioFormat.setInteger(MediaFormat.KEY_CHANNEL_MASK, AudioFormat.CHANNEL_IN_MONO);
		audioFormat.setInteger(MediaFormat.KEY_BIT_RATE, BIT_RATE);
		audioFormat.setInteger(MediaFormat.KEY_CHANNEL_COUNT, 1);

//		audioFormat.setLong(MediaFormat.KEY_MAX_INPUT_SIZE, inputFile.length());
//      audioFormat.setLong(MediaFormat.KEY_DURATION, (long)durationInMs );
		if (DEBUG) Log.i(TAG, "format: " + audioFormat);
		mMediaCodec = MediaCodec.createEncoderByType(MIME_TYPE);
		mMediaCodec.configure(audioFormat, null, null, MediaCodec.CONFIGURE_FLAG_ENCODE);
		mMediaCodec.start();
		if (DEBUG) Log.i(TAG, "prepare finishing");
		if (mListener != null) {
			try {
				mListener.onPrepared(this);
			} catch (final Exception e) {
				Log.e(TAG, "prepare:", e);
			}
		}
	}

	@Override
	protected void startRecording() {
		super.startRecording();
		// create and execute audio capturing thread using internal mic
		if (mAudioThread == null) {
			mAudioThread = new rcmobile.andruavmiddlelibrary.com.serenegiant.encoder.MediaAudioEncoder.AudioThread();
			mAudioThread.start();
		}
	}

	@Override
	protected void release() {
		try {
			if (mAudioThread != null) {
				// check as it might be called twice if you add a release in the Thread.
				mAudioThread.join();
			}
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		mAudioThread = null;
		if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.JELLY_BEAN_MR2) {
			super.release();
		}
	}


	public void encode(final ByteBuffer buffer) {

//    	if (DEBUG) Log.v(TAG, "encode:buffer=" + buffer);
		mEncodeCalled = true;
		synchronized (mSync) {
			if (!mIsCapturing || mRequestStop) return;
		}
		encode(buffer, getPTSUs());
	}



	/**
	 * Thread to capture audio data from internal mic as uncompressed 16bit PCM data
	 * and write them to the MediaCodec encoder.
	 * This is parallel to the Encoder thread that uses incomming data from Encode.
	 */
	private class AudioThread extends Thread {
		@Override
		public void run() {
//    		android.os.Process.setThreadPriority(android.os.Process.THREAD_PRIORITY_URGENT_AUDIO);
			try {

				final int min_sz = AudioRecord.getMinBufferSize(
						SAMPLE_RATE, AudioFormat.CHANNEL_IN_MONO, AudioFormat.ENCODING_PCM_16BIT);
				final int buf_sz = min_sz % 4 != 0 ? (min_sz / 4 + 1) * 4 : min_sz;
	            /*
	            BUG: MIC makes a bug with GCS !!
	             */
				final AudioRecord audioRecord = new AudioRecord(MediaRecorder.AudioSource.MIC, //.MIC,
						SAMPLE_RATE, AudioFormat.CHANNEL_IN_MONO, AudioFormat.ENCODING_PCM_16BIT, buf_sz);
				try {
					if (mIsCapturing) {
						if (DEBUG) Log.v(TAG, "AudioThread:startEncryptFile audio recording");
						final byte[] buf = new byte[buf_sz];
						int readBytes;
						audioRecord.startRecording();
						try {
							while (mIsCapturing && !mRequestStop && !mIsEOS) {
								// read audio data from internal mic
								readBytes = audioRecord.read(buf, 0, buf_sz);
								if (readBytes > 0) {
									// set audio data to encoder
									encode(buf, readBytes, getPTSUs());
									frameAvailableSoon();
								}
							}
							frameAvailableSoon();
						} finally {
							audioRecord.stop();
						}
					}
				}
				catch (final Exception ex) {
					ex.printStackTrace();
				}finally
				{
					if (!mEncodeCalled)
					{
						audioRecord.release();
					}
				}
			} catch (final Exception e) {
				Log.e(TAG, "AudioThread#run", e);
			}
			if (DEBUG) Log.v(TAG, "AudioThread:finished");
		}
	}

	/**
	 * select the first codec that match a specific MIME type
	 * @param mimeType
	 * @return
	 */
	@SuppressWarnings("deprecation")
	private static MediaCodecInfo selectAudioCodec(final String mimeType) {
		if (DEBUG) Log.v(TAG, "selectAudioCodec:");

		MediaCodecInfo result = null;
		// get the list of available codecs
		final int numCodecs = MediaCodecList.getCodecCount();
		LOOP:	for (int i = 0; i < numCodecs; i++) {
			final MediaCodecInfo codecInfo = MediaCodecList.getCodecInfoAt(i);
			if (!codecInfo.isEncoder()) {	// skipp decoder
				continue;
			}
			final String[] types = codecInfo.getSupportedTypes();
			for (int j = 0; j < types.length; j++) {
				if (DEBUG) Log.i(TAG, "supportedType:" + codecInfo.getName() + ",MIME=" + types[j]);
				if (types[j].equalsIgnoreCase(mimeType)) {
					if (result == null) {
						result = codecInfo;
						break LOOP;
					}
				}
			}
		}
		return result;
	}

}
